    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Disciplinas::destroy($id);
        $mensagem = 'Disciplina Excluida!';
        return view('Disciplinas.sucesso', compact('mensagem'));
